class Video:
    create_time: int
    '''
    UTC Unix epoch (in seconds) of when the TikTok video was posted.
    '''

    like_count: int
    '''
    Numbers of likes for the video
    '''

    comment_count: int
    '''
    Number of comments on the video
    '''

    share_count: int
    '''
    Number of shares of the video
    '''

    view_count: int
    '''
    Number of views of the video
    '''
